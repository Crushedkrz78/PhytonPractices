"""Estructuras de control - While"""

i = 1
while i <= 3:
    print(str(i))
    i += 1

#Ingresar un valor para definir numero de iteraciones
j = int(input("Ingrese un numero para comenzar: \n"))
k = 0
while k < j:
    print("Numero: "+str(k))
    k += 1
print("Fin del codigo")

